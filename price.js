var price_button_1 = document.getElementById("btn_pricing")
function changecolour(){
   price_button_1.style.backgroundColor="rgb(10, 12, 114)"
  
   
}

function chnage_text_color(){
   price_button_1.style.color="white"
}

function removecolor(){
    price_button_1.style.color="rgb(10, 12, 114)"
}

function removebackground_color(){
    price_button_1.style.backgroundColor="white"
}

 price_button_1.addEventListener('mouseover',changecolour)
 price_button_1.addEventListener('mouseover',chnage_text_color)
 price_button_1.addEventListener('mouseleave',removecolor)
 price_button_1.addEventListener('mouseleave',removebackground_color)



